package entidades;

public class Quarto {
	private int numeroQuarto;
	private int numeroLeitos;
	private int andarQuarto;
	
	public int getNumeroQuarto() {
		return numeroQuarto;
	}
	public void setNumeroQuarto(int numeroQuarto) {
		this.numeroQuarto = numeroQuarto;
	}
	public int getNumeroLeitos() {
		return numeroLeitos;
	}
	public void setNumeroLeitos(int numeroLeitos) {
		this.numeroLeitos = numeroLeitos;
	}
	public int getAndarQuarto() {
		return andarQuarto;
	}
	public void setAndarQuarto(int andarQuarto) {
		this.andarQuarto = andarQuarto;
	}
	
	

}
